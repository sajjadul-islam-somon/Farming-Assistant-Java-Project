    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        if (evt.getSource() == addBtn) {
            try {
                c = DriverManager.getConnection("jdbc:mysql:///oopdata", "root", "12345");
                String query = "insert into agridata(crope_name, soil_ph, soil_type, temp, rainfall, sunlight, period) values(?,?,?,?,?,?,?)";
                pst = c.prepareStatement(query);
                pst.setString(1, cropField.getText());
                pst.setString(2, phField.getText());
                pst.setString(3, typeField.getText());
                pst.setString(4, tempField.getText());
                pst.setString(5, rainField.getText());
                pst.setString(6, sunField.getText());
                pst.setString(7, periodField.getText());
                pst.execute();
                JOptionPane.showMessageDialog(null, "Saved ✅️");
            } catch(Exception e) {
                System.out.println(e);
            }
            showTable();
        }
    }//GEN-LAST:event_addBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        
        try {
            int selectedRow = jTable.getSelectedRow();
            String cropName = jTable.getValueAt(selectedRow, 0).toString();
            String deleteQuery = "DELETE FROM agridata WHERE crope_name = ?";
            pst = c.prepareStatement(deleteQuery);
            pst.setString(1,cropName);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Deleted ❌️");
        } catch(Exception e) {
            System.out.println(e);
        }
        showTable();
    }//GEN-LAST:event_deleteBtnActionPerformed

