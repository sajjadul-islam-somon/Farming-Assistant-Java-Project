    private void showTable() {
        try {
//            Class.forName(com.mysql.cj.jdbc.Driver);        //To register driver(now it's not needed)
            c = DriverManager.getConnection("jdbc:mysql:///oopdata", "root", "12345");
            s = c.createStatement();
            ResultSet rs = s.executeQuery("select * from agridata");
            ResultSetMetaData rsmd = rs.getMetaData();
            int N = rsmd.getColumnCount();
            DefaultTableModel model = (DefaultTableModel) jTable.getModel();
            model.setRowCount(0);
            
            while (rs.next()) {                                                 //Data will be added until finish
                Vector v = new Vector();
                for (int i=1; i<=N;i++) {
                    v.add(rs.getString("crope_name"));
                    v.add(rs.getString("soil_ph"));
                    v.add(rs.getString("soil_type"));
                    v.add(rs.getString("temp"));
                    v.add(rs.getString("rainfall"));
                    v.add(rs.getString("sunlight"));
                    v.add(rs.getString("period"));
                }
                model.addRow(v);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }

